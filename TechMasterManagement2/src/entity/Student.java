package entity;

import java.util.Date;

public class Student {

    private int id;
    private static int autoId=0;
    private String name;
    private Date dob;
    private String rank;

    public Student() {
        autoId++;
        this.id = autoId;
    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", dob=" + dob +
                ", rank='" + rank + '\'' +
                '}';
    }
}
